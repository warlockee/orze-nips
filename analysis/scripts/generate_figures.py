"""
Script 6: Generate all paper figures.

Reads computed values from doc/computed_values/ (generated by other scripts)
and produces publication-quality PDF figures in doc/figures/.

Figures:
  1. Search loop + configuration space diagram (schematic)
  2. Convergence curves (main result figure)
  3. Multi-agent dynamics (entropy + JSD)
  4. Backbone x encoder heatmap
  5. Backbone distribution shift + AP trajectory with bug-fix annotations

Outputs:
  - doc/figures/figure1_schematic.pdf
  - doc/figures/convergence.pdf  (generated by compute_convergence.py, enhanced here)
  - doc/figures/agent_dynamics.pdf (generated by compute_agent_dynamics.py, enhanced here)
  - doc/figures/heatmap.pdf
  - doc/figures/trajectory.pdf
"""

import json
import os
import sys
import glob
import numpy as np
from collections import defaultdict
